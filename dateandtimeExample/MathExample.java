package com.dateandtimeExample;

public class MathExample {
    public  static  void main(String[] args){
       int a=45,b=28;

        System.out.println(Math.max(a,b));//45
        System.out.println(Math.min(a,b));//28
        System.out.println(Math.sqrt(a));//5.291502622129181
        System.out.println(Math.pow(a,b));//1.3246813146595588E65
        System.out.println(Math.exp(a*2));//1.446257064291475E12
//        System.out.println(Math.toRadians(a));
        System.out.println(Math.sin(a));
        System.out.println(Math.tan(a));


    }
}
